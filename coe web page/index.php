         <p align="left">

           The Office of the Controller of Examinations has been functioning from the year 1987 with the conferment of Autonomous status to the college. The College invests time and evolves strategies to bring about qualitative improvement in Examination management.
         </p>


         <p align="left">
The Examinations Office operates within the prescribed parameters of the Madurai Kamaraj  University in matters pertaining to minimum course requirements leading to undergraduate, postgraduate, and Ph.D. programmes and also the semester-based Choice Based Credit System, (CBCS),  Cumulative Grade Point Average, (CGPA),  and the ten-point Grading scale, due to the institutional’s affiliation with the University.

         </p>

         <p align="left">
           The system of evaluation has been systematically and periodically remodelled to make it highly credible and valid. The examination process and the evaluation reforms are in tune with the modernizing the curriculum and syllabus, pedagogical tools and techniques,  breaking the tight boundaries between disciplines, introduction of add- on courses, increasing the number of Generic and Discipline Specific Electives, and most importantly,  evaluation reforms. Autonomy allows the institution (Arul Anandar College)  and in turn the Office of the  Controller of Examinations  to rethink and reframe the pattern and testing methodology, set rubrics, introduce new norms, for qualitative change, and to raise the overall standards to help the student community to face the 21st century challenges.
         </p>
        

           <p align="left">
Comprehensive evaluation of the capabilities of the students is facilitated through the graded question paper pattern and evaluation components. The evaluation pattern is designed to test the textual comprehension and practical application of knowledge gained in various areas of their chosen subjects. Hence, credits and marks are allotted for projects, internships and training programmes.
         </p>

         <p align="left">
           Arul Anandar College is consistent with the guidelines of UGC and Madurai Kamaraj University for the conduct of examinations. It uses the Enterprise Resource Planning (ERP) for Examination Management.
         </p>
         
           <p align="left">
Mechanisms are framed to evaluate the academic performance of students after approval from the Academic Council. The Academic Programmes and Regulations are available at the College website which guarantees that stakeholders are apprised of the evaluation procedures.
